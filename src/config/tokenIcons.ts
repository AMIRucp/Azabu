export function getIcon(symbol: string): { type: 'img' | 'emoji' | 'letter'; value: string } {
  const s = symbol.toUpperCase()
    .replace(/USDT$/, '').replace(/USDC$/, '')
    .replace(/-PERP$/, '').replace(/-USD$/, '')
    .replace(/USD\d*$/, '')
    .replace(/^1000/, '').replace(/^1M/, '').replace(/^1K/, '');

  if (ICONS[s]) return { type: 'img', value: ICONS[s] };
  if (FLAGS[s]) return { type: 'emoji', value: FLAGS[s] };
  if (EMOJIS[s]) return { type: 'emoji', value: EMOJIS[s] };

  const raw = symbol.toUpperCase();
  if (ICONS[raw]) return { type: 'img', value: ICONS[raw] };
  if (FLAGS[raw]) return { type: 'emoji', value: FLAGS[raw] };
  if (EMOJIS[raw]) return { type: 'emoji', value: EMOJIS[raw] };

  return { type: 'letter', value: (s || raw).slice(0, 5) };
}

export function getIconWithJupiter(
  symbol: string,
  jupiterLogos: Record<string, string>,
): { type: 'img' | 'emoji' | 'letter'; value: string } {
  const local = getIcon(symbol);
  if (local.type !== 'letter') return local;

  const s = symbol.toUpperCase()
    .replace(/USDT$/, '').replace(/USDC$/, '')
    .replace(/-PERP$/, '').replace(/-USD$/, '')
    .replace(/USD\d*$/, '')
    .replace(/^1000/, '').replace(/^1M/, '').replace(/^1K/, '');

  if (jupiterLogos[s]) return { type: 'img', value: jupiterLogos[s] };
  if (jupiterLogos[symbol.toUpperCase()]) return { type: 'img', value: jupiterLogos[symbol.toUpperCase()] };

  return local;
}

export function getCmcIcon(symbol: string): string | null {
  const icon = getIcon(symbol);
  if (icon.type === 'img') return icon.value;
  if (icon.type === 'emoji') return `emoji:${icon.value}`;
  return null;
}

